			<h1 class="float_left">História do Lar</h1>
        <div class="clear"></div>
        <p>O Lar de Crianças Sara e Burton Davis  foi  fundado em 2001  com o objetivo de  abrigar crianças e adolescentes  vítimas de vulnerabilidade social.<br /><br />
A ideia surgiu  de uma senhora  chamada Betty Brown, visitou o Brasil e se impressionou com o número de crianças vitimizadas. Sendo assim, ela doou parte de sua herança para construir um abrigo que cuidasse dessas crianças.
O nome da instituição LAR DE CRIANÇAS SARA E BURTON DAVIS foi uma homenagem  feita a um casal de médicos e missionários que trabalharam muito anos no Brasil, defendendo as  causas sociais.
O Lar Davis é mantido apenas por doações voluntárias, ofertas recebidas de  igrejas  e parcerias com algumas empresas.</p>

		<h1>Visão</h1>
        <p>Ser uma rede de atendimento às crianças e adolescentes que estão em situação de vulnerabilidade social no Brasil.</p>
        
        <h1>Missão</h1>
        <p>Acolher e propiciar cuidado integral, às crianças e adolescentes que sofreram negligência e violência, oportunizando que se tornem cidadãos dignos e produtivos, priorizando o seu encaminhamento a uma convivência familiar saudável.</p>           	
